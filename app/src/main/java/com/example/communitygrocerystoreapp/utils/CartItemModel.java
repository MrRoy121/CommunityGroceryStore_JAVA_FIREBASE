package com.example.communitygrocerystoreapp.utils;

public class CartItemModel {
    String name, price, id;
    int count;


    public CartItemModel(String name, int count, String price, String id) {
        this.name = name;
        this.count = count;
        this.price = price;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }
}
